    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(lit.hasNext() == true && lit2.hasNext() == true){
            if(alt == false){
                jTextArea1.setText(lit.next());
                alt = true;
            }
            else{
                jTextArea1.setText(lit2.next());
                alt = false;
            }
        }
        else{
            //Actualizar el plan sin la accion ya realizada
            change = "subtema_ensenado(S)";
            band = true;
            Update.updatePlan(change, band);
            status = Dlearning.dlv();
            
            if(status.startsWith("aplicar_quiz")){
                //Falta lanzar popup de quiz
                QuizFrm quiz_subt = new QuizFrm(user);
                quiz_subt.setVisible(true);
                this.setVisible(false);
            }
            
            if(status.startsWith("ir_asesorias")){
                //Falta lanzar popup de asesorias
                ApuntesFrm apuntes = new ApuntesFrm(user);
                apuntes.setVisible(true);
                this.setVisible(false);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

