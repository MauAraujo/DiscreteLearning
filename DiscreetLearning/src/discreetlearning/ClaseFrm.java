    public void Step(){
        //Aqui se van a escoger los archivos dependiendo del subtema
        try{
            switch(status){
                case "ensenar_subtema(primero);": teoria = Files.readAllLines(Paths.get("src/Files/ConceptosRelaciones.txt"));
                                                  lit = teoria.listIterator();
                                                  ejercicios = Files.readAllLines(Paths.get("src/Files/Ejemplos.txt"));
                                                  lit2 = ejercicios.listIterator();
                                                  jTextArea1.setText(teoria.get(cont));
                                                  break;
                                                  
                case "ensenar_subtema(segundo);": break;
                case "ensenar_subtema(tercero);": break;
                default: System.out.println("No hay subtema");
                         break;
            }
        }catch(Exception e){
            e.printStackTrace();        
        }
    }
    
