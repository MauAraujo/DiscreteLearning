    private void iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarActionPerformed
        // TODO add your handling code here:
        passwd = new String (password.getText());
        user = username.getText();
        f = new File("credenciales.txt");
        StringBuilder strb = new StringBuilder();
        
        if(f.exists()){
            try{
                fin = new BufferedReader(new FileReader(f));
                while ((line = fin.readLine()) != null){
                    temp = line.split(":", 2);
                    if(temp.length >= 2){
                        usr = decrypt(temp[0]);
                        pass = decrypt(temp[1]);
                        credenciales.put(usr, pass);
                        
                    }
                }
                fin.close();
            }catch(IOException e){}
        }
        
        
        if(credenciales.containsKey(user)){
            if(passwd.equals(credenciales.get(user))){
                JOptionPane.showMessageDialog(null, "Ingresando a su cuenta.", "Exito", JOptionPane.INFORMATION_MESSAGE); 
                
                
                
                //frmTest test = new frmTest(user);
                TemaSelect tema = new TemaSelect(user);
                tema.setVisible(true);
                this.setVisible(false);
                //this.dispose();
                
                
            }
            else
                JOptionPane.showMessageDialog(null, "Contraseña incorrecta.", "Error", JOptionPane.ERROR_MESSAGE);
                password.setText("");
        }
        else
            JOptionPane.showMessageDialog(null, "Usuario no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
            username.setText("");
            password.setText("");
    }//GEN-LAST:event_iniciarActionPerformed

     public String encrypt(String info){
        byte[] bit = info.getBytes();
        String encrypted;
        encrypted = Base64.getEncoder().encodeToString(bit);
        return encrypted;
    }
    
    public String decrypt(String info){
        byte bit[] = Base64.getDecoder().decode(info);
        String decrypted = new String(bit);
        return decrypted;
    }
