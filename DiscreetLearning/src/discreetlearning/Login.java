    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        // TODO add your handling code here:
        passwd = new String(password.getText());
        user = username.getText();
        fields.put(user, passwd);
        f = new File("credenciales.txt");
        StringBuilder strb = new StringBuilder();
        
        if(f.exists()){
            try{
                fin = new BufferedReader(new FileReader(f));
                while (fin.ready()){
                    strb.append(fin.readLine()).append(System.lineSeparator());
                }
                fin.close();
            }catch(IOException e){}
            strb.append(encrypt(user)).append(":").append(encrypt(fields.get(user))).append(System.lineSeparator());
        }
        
        else{   
            for (String k : fields.keySet())
                strb.append(encrypt(k)).append(":").append(encrypt(fields.get(k))).append(System.lineSeparator());
        }   
        
        try{
            fout = new FileWriter(f);
            fout.write(strb.toString());
            fout.flush();
            fout.close();
        }catch(IOException e){}
        JOptionPane.showMessageDialog(null, "Usuario registrado.", "Exito", JOptionPane.INFORMATION_MESSAGE); 
        username.setText("");
        password.setText("");
    }//GEN-LAST:event_registrarActionPerformed

